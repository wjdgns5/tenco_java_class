package basic.ch04;

public class IfElseTest {

	// 코드의 시작점
	public static void main(String[] args) {
		
		int age = 20;
		
		if (age >= 20) {
			System.out.println("고등학교를 졸업 했습니다. ");
		} else {
			System.out.println("학교를 다니고 있습니다. ");
		}
		// if-else --> 둘 중 하나는 반드시 수행 된다.
	
	} // end of main

} // end of class
